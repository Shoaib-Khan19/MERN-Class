
// Area of Cirlce
exports.areaOfCircle = function(r){
    const pi=3.14;
    return pi*r*r;
}
// Area of SQaure
exports.areaOfSquare=function (w,h){
    return w*h;
}
// Area of Traingle
exports.areaOfTriangle=function (b,h){
    return 1/2*(b*h);
}
